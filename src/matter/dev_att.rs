impl DevAttDataFetcher for HardCodedDevAtt {
    fn get_devatt_data(&self, data_type: DataType, data: &mut [u8]) -> Result<usize, Error> {
        let src = match data_type {
            DataType::CertDeclaration => CD,
            DataType::DAC => DAC_CERT,
            DataType::DACPrivKey => DAC_KEY,
            DataType::DACPubKey => {
                // Public key is embedded in DAC certificate, return empty
                return Ok(0);
            }
            DataType::PAI => PAI_CERT,
        };

        if src.len() > data.len() {
            return Err(ErrorCode::BufferTooSmall.into());
        }

        data[..src.len()].copy_from_slice(src);
        Ok(src.len())
    }
}
